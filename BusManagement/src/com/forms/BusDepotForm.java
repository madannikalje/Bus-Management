    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
        try {
            String name=txtname.getText();
            String address=txtaddress.getText();
            String contactnos=txtcontactnos.getText();
            String contactperson=txtcontactperson.getText();
            
            String email=txtemail.getText();
            String website=txtwebsite.getText();
            //if record exists then update otherwise insert
            ResultSet rs=db.getRows("select * from BusDepot");
            if(rs.next())
            {
                db.execute("Update BusDepot set Name=?,Address=?,ContactNo=?,ContactPerson=?,EmailId=?,WebSite=?",name,address,contactnos,contactperson,email,website);
            }
            else
            {
                db.execute("Insert into BusDepot values(?,?,?,?,?,?)",name,address,contactnos,contactperson,email,website);
            }
            JOptionPane.showMessageDialog(this, "Bus Depot Information Updated");
            
            dispose();
        } catch (SQLException ex) {
            Logger.getLogger(BusDepotForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnsubmitActionPerformed

