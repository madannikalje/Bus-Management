    public BusDepotForm() {        
        try {
            initComponents();
            Utility.setLabelImage(lblimage, new File("src/images/bus2.jpg").getAbsolutePath());
            //Get Shop Information
            ResultSet rs=db.getRows("select * from BusDepot");
            if(rs.next())
            {
                txtname.setText(rs.getString(1));
                txtaddress.setText(rs.getString(2));
                txtcontactnos.setText(rs.getString(3));
                txtcontactperson.setText(rs.getString(4));
                txtemail.setText(rs.getString(5));
                txtwebsite.setText(rs.getString(6));
            }
            //getRootPane().setDefaultButton(btnsubmit);
            
            //Validation
            txtname.setInputVerifier(new InputVerifier() {
                @Override
                public boolean verify(JComponent input) {
                    String name=txtname.getText();
                    if(name.equals("")){
                        lblerror.setText("Name cannot be left empty");
                        return false;
                    }else{
                        if(name.matches("^[a-zA-Z ]+$")){  // ^RegExPattern$
                            lblerror.setText("");
                            return true;
                        }else{
                            lblerror.setText("Name allows only letters and spaces");
                            return false;
                        }
                    }
                }
            });
            
            txtcontactnos.setInputVerifier(new InputVerifier() {
                @Override
                public boolean verify(JComponent input) {
                    String mobile=txtcontactnos.getText();
                    if(mobile.equals("")){
                        lblerror.setText("ContactNos required");
                        return false;
                    }else{
                        lblerror.setText("");
                        return true;                        
                    }
                }
            });
            
            txtcontactperson.setInputVerifier(new InputVerifier() {
                @Override
                public boolean verify(JComponent input) {
                    String s=txtcontactnos.getText();
                    if(s.equals(""))
                    {
                        lblerror.setText("Contact Person cannot be left blank");
                        return false; //Validation failed
                    }
                    else{
                        lblerror.setText("");
                        return true; //Validation OK
                    }
                    
                }
            });
        } catch (SQLException ex) {
            Logger.getLogger(BusDepotForm.class.getName()).log(Level.SEVERE, null, ex);
        }
                
                
            
    }

