    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // Edit
        int rowindex=table1.getSelectedRow();
        if(rowindex!=-1)
        {
        String id=table1.getValueAt(rowindex, 0)+"";
        edit(id);        
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Error:::Please select row which you have to edit...");
        }
    }//GEN-LAST:event_btneditActionPerformed
    void edit(String id)
    {
        try {
            
            
            ResultSet rs=db.getRows("select * from Employee where EmpNo=?", id);
            if(rs.next())
            {
                String name=rs.getString(2);
                String address=rs.getString(3);
                String bdate=rs.getString(4);
                String jdate=rs.getString(5);
                String contact=rs.getString(6);
                String email=rs.getString(7);
                String desg=rs.getString(8);
                
                txtid.setText(id);
                txtname.setText(name);
                txtaddress.setText(address);
                txtcontact.setText(contact);
                txtbdate.setDate(LocalDate.parse(bdate));
                txtjdate.setDate(LocalDate.parse(jdate));
                txtemail.setText(email);
                cmbdesg.setSelectedItem(desg); 
                InputStream in=rs.getBinaryStream(9);
                try {
                    file=Utility.getFileFromStream(in);
                } catch (Exception ex) {
                    Logger.getLogger(EmployeeForm.class.getName()).log(Level.SEVERE, null, ex);
                }
                db.setImageToLabel(file, lblphoto);
                
                btnsave.setText("Update");
                txtname.requestFocus();
            }
        } catch (SQLException ex) {
            Logger.getLogger(EmployeeForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // Delete
        int val=JOptionPane.showConfirmDialog(this, "Do you want delete selected record?","Confirm Delete",JOptionPane.YES_NO_OPTION);
        if(val==JOptionPane.YES_OPTION)
        {
        int rowindex=table1.getSelectedRow();
        String id=table1.getValueAt(rowindex, 0)+"";
        db.execute("delete from Employee where EmpNo=?", id);
        db.fillTable(table1, "select * from Employee",8,cols);  
        db.fillCombo(cmbempno, "select * from Employee", "EmpNo", "EmpNo");
        }
    }//GEN-LAST:event_btndeleteActionPerformed

