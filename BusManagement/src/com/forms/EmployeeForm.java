    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // Save      
        if(txtbdate.getText().equals(""))
        {
            lblerror.setText("Birth Date is Required");
            txtbdate.requestFocus();
            return;
        }
        if(txtjdate.getText().equals(""))
        {
            lblerror.setText("Join Date is Required");
            txtjdate.requestFocus();
            return;
        }
        String id=txtid.getText();
        String name=txtname.getText();
        String address=txtaddress.getText();        
        String bdate=txtbdate.getDate().format(DateTimeFormatter.ISO_DATE);
        String jdate=txtjdate.getDate().format(DateTimeFormatter.ISO_DATE);
        String contact=txtcontact.getText();
        String email=txtemail.getText();
        String desg=cmbdesg.getSelectedItem()+"";
        
        String btntext=btnsave.getText().toLowerCase();
        if(btntext.equals("save"))
        {
            if(file==null)
            {
                lblerror.setText("Employee Image is Required");
                return;
            }
            db.execute("Insert into Employee (EmpName,EmpAddress,BirthDate,JoinDate,ContactNo,EmailId,Designation,Photo) values(?,?,?,?,?,?,?,?)",8,file, name,address,bdate,jdate,contact,email,desg,"");
            JOptionPane.showMessageDialog(this, "Employee is Saved Successfully");            
        }
        else
        {
            db.execute("Update Employee set EmpName=?,EmpAddress=?,BirthDate=?,JoinDate=?,ContactNo=?,EmailId=?,Designation=?,Photo=? where EmpNo=?",8,file,name,address,bdate,jdate,contact,email,desg,"",id);            
            JOptionPane.showMessageDialog(this, "Employee is Updated Successfully");
        }
        db.fillTable(table1, "select * from Employee",8,cols); 
        db.fillCombo(cmbempno, "select * from Employee", "EmpNo", "EmpNo");
        btnreset.doClick();
    }//GEN-LAST:event_btnsaveActionPerformed

