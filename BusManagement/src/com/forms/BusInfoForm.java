    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        String no=txtno.getText();
        String type=cmbtype.getSelectedItem()+"";
        String seats=txtseats.getText();
        String seats1=txtseats1.getText();
        String seats2="0";
        
        String opr=btnsave.getText().toLowerCase();
        if(opr.equals("save"))
        {
            db.execute("Insert into Bus values(?,?,?,?,?)", no,type,seats,seats1,seats2);
            JOptionPane.showMessageDialog(this, "Bus Information Saved");
        }
        else
        {
            db.execute("Update Bus set BusType=?,Seats=?,ASeats=?,Reserved=? where BusNo=?", type,seats,seats1,seats2,no);
            JOptionPane.showMessageDialog(this, "Bus Information Updated");
            
        }
        db.fillTable(table1, "select * from Bus");
        btnreset.doClick();
    }//GEN-LAST:event_btnsaveActionPerformed

