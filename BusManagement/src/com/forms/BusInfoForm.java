    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        int val=JOptionPane.showConfirmDialog(this, "Do you want to delete selected record?","Confirm Delete",JOptionPane.YES_NO_OPTION);
        if(val==JOptionPane.YES_OPTION)
        {
        int rowindex=table1.getSelectedRow();
        String id=table1.getValueAt(rowindex, 0)+"";
        db.execute("delete from Bus where BusNo=?", id);
        db.fillTable(table1, "select * from Bus");
        }
    }//GEN-LAST:event_btndeleteActionPerformed

